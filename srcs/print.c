#include <head.h>

void	print(char **str, int l)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (i < l)
	{
		while (j < l)
		{
			ft_putchar(str[i][j]);
			j++;
		}
		j = 0;
		i++;
		ft_putchar('\n');
	}
}
